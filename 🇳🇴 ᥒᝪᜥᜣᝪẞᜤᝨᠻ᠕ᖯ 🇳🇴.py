    async def pwablcmd(self, message):
        """сохраняет шаблон в браузер"""
        name = utils.get_args_raw(message)
        reply = await message.get_reply_message()
        if reply:
            await message.edit('Пᴏдᴏждиᴛᴇ...')
            if reply.text:
                text = reply.text
                fname = f'{name or message.id + reply.id}.txt'
                file = open(fname, 'w')
                file.write(text)
                file.close()
                await message.edit(
                    f'Фᴀйᴧ ᴄᴏхᴩᴀнён ᴋᴀᴋ: <code>{fname}</code>')
            else:
                ext = reply.file.ext
                fname = f'{name or message.id + reply.id}{ext}'
                await message.client.download_media(reply, fname)
                await message.edit(
                    f'Фᴀйᴧ ᴄᴏхᴩᴀнён ᴋᴀᴋ: <code>{fname}')
        else:
            return await message.edit('<b>Рᴇᴨᴧᴀй нᴀ ɸᴀйᴧ ᴛxᴛ ɸᴏᴩʍᴀᴛᴀ иᴧи ᴛᴇᴋᴄᴛ!</b>')

